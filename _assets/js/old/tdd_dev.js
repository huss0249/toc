globalThis.reuse_dev = (logType, message, str) => {
    /*
    ======================================================
    USAGE:
            - Hide the alerts and logs when dev mode is not present
    INPUT:
            -
    RETURN:
            -
    ======================================================
    TO DO:
            -
    ======================================================
    TO FIX:
            -
    ======================================================
    */
    if (developer) {
        if (logType === 'alert') {
            if(str) {
                alert(`${str} ==> ${message}`);
            } else {
                alert(message);
            }
//            alert(`${str} ==> ${message}`);
        }
        if (logType === 'log') {
            if(str) {
                console.log(`${str} ==> ${message}`);
            } else {
                console.log(message);
            }
//            console.log(`${str} ==> ${message}`);
        }
    }
}


